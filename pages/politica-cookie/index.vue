          terțe părți prin intermediul bannerelor, boxurilor sau linkurilor - iar toate aceste
          instrumente pot conține cookie-uri. Ele se numesc “third party cookies“ pentru
          că nu sunt plasate de proprietarul website-ului respectiv, iar furnizorii terți se
          supun legilor îin vigoare și politicilor de confidențialitate ale deținătorului
          site-ului.
        </p>
