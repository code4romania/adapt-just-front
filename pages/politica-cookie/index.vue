        <p class="content-text" data-listen-text>
          Cookie-urile reprezintă punctul central al funcționării eficiente a Internetului,
          ajutând la generarea unei experiențe de navigare prietenoase și adaptată
          preferințelor și intereselor fiecărui utilizator. Refuzarea sau dezactivarea
          cookieurilor poate face unele site-uri imposibil de folosit.
        </p>
        <p class="content-text" data-listen-text>
          Refuzarea sau dezactivarea cookie-urilor nu înseamnă că nu veți mai primi
