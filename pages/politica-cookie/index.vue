          ultimul rând pot permite aplicațiilor multimedia sau de alt tip de pe alte site-uri
          să fie incluse într-un anumit site pentru a face navigarea mai utilă.
        </p>
        <h1 class="content-text font-weight-bold" data-listen-text>Ce este un “cookie“?</h1>
        <p class="content-text" data-listen-text>
          Cookie (“browser cookie“ sau “HTTP cookie“) este un fișier de mici
          dimensiuni, format din litere și numere, care este stocat pe orice terminal cu
          acces la internet (computer, telefon mobil, tabletă etc.) și este instalat prin
          solicitare emisă de către un web-server unui browser (ex: Internet Explorer,
          Chrome). De reținut: “Cookie“-urile nu conțin programe software, viruși sau
          spyware și nu pot accesa informațiile de pe hard drive-ul utilizatorului.
          Un cookie este format din nume și conținut, durata de existență a acestuia
          fiind determinată, putând fi accesat din nou de webserver în momentul în care
          un utilizator se întoarce pe website-ul asociat webserverului respectiv.
          Cookie-urile nu solicită informații cu caracter personal și nu identifică
          personal utilizatorii de internet.
        </p>
        <p class="content-text" data-listen-text>
          Cookieuri de sesiune - acestea sunt stocate temporar în istoricul browser-ului
          care le memorează până când utilizatorul iese de pe web-siteul respectiv sau
          închide fereastra browserului.
        </p>
        <p class="content-text" data-listen-text>
          Cookieuri Persistente - Acestea sunt stocate, în funcție de durata prestabilită,
          pe hard-drive-ul unui computer sau echipament. Cookie-urile persistente le
          includ și pe cele plasate de un alt website decât cel pe care îl vizitează
          utilizatorul la momentul respectiv - cunoscute sub numele de “third party
          cookies“ - care pot fi folosite în mod anonim pentru a memora interesele unui
          utilizator, astfel încât să fie livrată publicitate cât mai relevantă pentru
          utilizatori.
        </p>
        <h1 class="content-text font-weight-bold" data-listen-text>Care sunt avantajele cookie-urilor?</h1>
        <p class="content-text" data-listen-text>
          Un cookie conține informații care fac legătura între utilizatori și un anume
          website. Dacă un browser accesează acel web-server din nou, acesta poate
          citi informația deja stocată și reacționa în consecință.
        </p>
        <h1 class="content-text font-weight-bold" data-listen-text>Care este durata de viață a unui cookie?</h1>
        <p class="content-text" data-listen-text>
