          Cookie-urile păstrează informații într-un fișier text de mici dimensiuni care
          permit unui website să recunoască un browser. Webserver-ul va recunoaște
          browserul până când cookie-ul expiră sau este șters. Cookie-ul stochează
          informații importante care îmbunătățesc experiența de navigare pe Internet
