          prețuri afișate în moneda națională. “Cookie“-urile, pe baza informațiilor pe
          care le adună despre utilizatori, îi ajută pe deținătorii de site-uri să își
          eficientizeze produsul astfel încât acesta să fie cât mai ușor accesat de către
